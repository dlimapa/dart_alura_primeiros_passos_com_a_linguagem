void main() {
  // for (variaval; condicao; o que fazer com a variavel)
  for (int contador = 0; contador < 10; contador++) {
    print("o contador é $contador");
  }

  int contador;

  for (contador = 10; contador >= 0; contador--) {
    print("o contador é $contador");
  }

  print("Finalizando contador $contador");
}
